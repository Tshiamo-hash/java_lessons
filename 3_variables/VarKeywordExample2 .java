public class VarKeywordExample2 {
	public static void main(String[] args){

		 var a = 100;
		 var b = 20.5;
		 var c = 'd';
		 var d = "Doll";
		 var e = false;
		
		a = 10;
		b = a;
		c = 'e';
		d = "cat";
		e = true;
		
        System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);

	}
}
